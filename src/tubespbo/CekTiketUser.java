    private void shows() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No KTP");
        model.addColumn("Nama Kereta");
        model.addColumn("Kota Asal");
        model.addColumn("Kota Tujuan");
        model.addColumn("Jumlah Tiket");
        model.addColumn("Waktu keberangkatan");
        
        Koneksi koneksi = new Koneksi();
        
        try {
            String sql = "SELECT transaksi.no_ktp, kereta.nama_kereta, kereta.keberangkatan, kereta.tujuan, transaksi.jml_tiket, kereta.waktu_keberangkatan FROM accounts JOIN transaksi ON accounts.id = transaksi.id_user JOIN kereta ON transaksi.id_kereta = kereta.id_kereta WHERE accounts.id = " + id_user;
            java.sql.Connection conn = (Connection)koneksi.koneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getInt(1),res.getString(2) ,res.getString(3) ,res.getString(4), res.getString(5), res.getString(6)});                
            }
            tabelTransaksi.setModel(model);
            
        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());
        }
    }

