    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        try {
            Koneksi conn = new Koneksi();
        conn.koneksi();
        
        ResultSet rs = conn.koneksi().createStatement().executeQuery("SELECT * FROM accounts WHERE nama='"+userTF.getText()+"' AND password='"+passTF.getText()+"' ");
        if(rs.next()){
            if ("0".equals(rs.getString("status"))) {
                JOptionPane.showMessageDialog(null, "Berhasil Masuk");
                new user().setVisible(true);
                this.dispose();
            }
            else if("1".equals(rs.getString("status"))) {
                JOptionPane.showMessageDialog(null, "Berhasil Masuk Sebagai admin");
                new admin().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Login");
            }
         }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_loginBtnActionPerformed

